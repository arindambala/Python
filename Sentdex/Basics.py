# Strings
progLang = "Python", "JavaScript", "Java", "C/C++" # Variable - Immutable
print(type(progLang)) # <class 'tuple'>
print(progLang) # ('Python', 'JavaScript', 'Java', 'C/C++')

# Loops
for langauge in progLang: # Temporary variable
    print(langauge)
    

# Lists
matx = [0, 0, 0,
        0, 0, 0,
        0, 0, 0,] # Variable - Mutable
print(type(matx)) # <class 'list'>
print(matx) # [0, 0, 0, 0, 0, 0, 0, 0, 0]